    { title: "Analisis jaringan", date: "2024-04-15", content: "Analisis Jaringan" }
  ];

  // Function to add a new blog post
  function addBlogPost(title, date, content) {
    var blogList = document.querySelector('.blog-list');
    var li = document.createElement('li');
    li.innerHTML = `
      <h3>${title}</h3>
      <p>Date: ${date}</p>
      <p>${content}</p>
      <button onclick="editBlogPost(this)">Edit</button>
      <button onclick="deleteBlogPost(this)">Delete</button>
    `;
    blogList.appendChild(li);
  }

  // Function to edit a blog post
  function editBlogPost(button) {
    var post = button.parentElement;
    var title = post.querySelector('h3').textContent;
    var date = post.querySelector('p:nth-of-type(1)').textContent.replace('Date: ', '');
    var content = post.querySelector('p:nth-of-type(2)').textContent;
    document.getElementById('blog-title').value = title;
    document.getElementById('blog-date').value = date;
    document.getElementById('blog-content').value = content;
    // Additional code to handle editing functionality
  }

  // Function to delete a blog post
  function deleteBlogPost(button) {
    var post = button.parentElement;
    post.remove();
    // Additional code to handle deletion from backend/database
  }

  // Function to handle form submission
  document.getElementById('blog-form').addEventListener('submit', function(event) {
    event.preventDefault(); // Prevent default form submission

    var title = document.getElementById('blog-title').value;
    var date = document.getElementById('blog-date').value;
    var content = document.getElementById('blog-content').value;

    // Check if this is an update or a new post
    var isUpdate = document.querySelector('.blog-list li h3').textContent === title;

    if (isUpdate) {
      // If it's an update, find the existing post and update its content
      var existingPost = document.querySelector('.blog-list li h3');
      existingPost.nextElementSibling.textContent = "Date: " + date;
      existingPost.nextElementSibling.nextElementSibling.textContent = content;
    } else {
      // If it's a new post, add it to the list
      addBlogPost(title, date, content);
    }

    // Reset form fields
    document.getElementById('blog-title').value = '';
    document.getElementById('blog-date').value = '';
    document.getElementById('blog-content').value = '';
  });

  // Adding sample blog posts on page load
  window.onload = function() {
    sampleData.forEach(function(post) {
      addBlogPost(post.title, post.date, post.content);
    });
  };
